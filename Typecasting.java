public class Typecasting
{
    public static void main(String[] args)
    {
        int num=2;
        int num2=3;
        int sum=(num+num2);
        System.out.println((float)sum);
        short s1=12345;
        System.out.println((int)s1);
        float num3=3.14f;
        System.out.println((int)num3);
        System.out.println(num+" , " +num2);

    }
}
